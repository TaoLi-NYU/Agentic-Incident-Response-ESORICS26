import argparse
import json
import re
import tempfile
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple


SYSTEM_MARKER = "### System:\n"
LOGS_MARKER = "\n\n### Logs:\n"
INSTRUCTION_MARKER = "\n\n### Instruction:\n"


def extract_section(text: str, start_marker: str, end_marker: str) -> Optional[str]:
    if start_marker not in text or end_marker not in text:
        return None
    start = text.index(start_marker) + len(start_marker)
    end = text.index(end_marker, start)
    return text[start:end].strip()


def extract_system_and_logs(instruction: str) -> Tuple[Optional[str], Optional[str]]:
    system = extract_section(instruction, SYSTEM_MARKER, LOGS_MARKER)
    logs = extract_section(instruction, LOGS_MARKER, INSTRUCTION_MARKER)
    return system, logs


def extract_final_json(answer: str) -> Optional[Dict[str, Any]]:
    decoder = json.JSONDecoder()
    matches = list(re.finditer(r"\{", answer))
    for match in reversed(matches):
        try:
            obj, _ = decoder.raw_decode(answer[match.start():])
        except json.JSONDecodeError:
            continue
        if isinstance(obj, dict) and (
            "Incident" in obj or "MITRE ATT&CK Tactics" in obj
        ):
            return obj
    return None


def normalize_incident(value: Any) -> str:
    incident = str(value).strip()
    if incident.lower() == "yes":
        return "Yes"
    if incident.lower() == "no":
        return "No"
    return incident


def normalize_tactics(value: Any) -> List[str]:
    if not isinstance(value, list):
        return []
    tactics: List[str] = []
    for item in value:
        tactic = str(item).strip()
        if tactic and tactic not in tactics:
            tactics.append(tactic)
    return tactics


def build_instruction(system: str, logs: str) -> str:
    return (
        "Below is a system description, a sequence of network logs (e.g., from an intrusion detection system), "
        "and an instruction that describes a task.\n"
        "Write a response that appropriately completes the request.\n"
        "Before generating the response, think carefully about the system, the logs, and the instruction.\n\n"
        f"### System:\n{system}\n\n"
        f"### Logs:\n{logs}\n\n"
        "### Instruction:\n"
        "You are a security operator with advanced knowledge in cybersecurity and IT systems.\n"
        "You have been given information about a system and some logs generated by it, e.g., security alerts.\n"
        "Your task is to determine whether the logs indicate a cyber incident that requires recovery actions.\n"
        "If the logs only show normal activity, unrelated security noise, or minor alerts that do not warrant "
        "recovery actions, set Incident to 'No'. Otherwise set Incident to 'Yes'.\n"
        "You should also identify the MITRE ATT&CK tactics that are directly supported by the logs and system "
        "description.\n"
        "Only include tactics that are explicitly supported by the evidence. Do not infer unsupported tactics.\n"
        "Return a JSON object with exactly two fields: 'Incident' and 'MITRE ATT&CK Tactics'.\n"
        "'Incident' must be either 'Yes' or 'No'.\n"
        "'MITRE ATT&CK Tactics' must be an array of strings.\n"
        "If Incident is 'No', return an empty tactics array unless the evidence directly supports a tactic.\n"
        "Return only the JSON with those two fields, nothing else.\n\n"
        "### Response:\n"
    )


def build_answer(incident: str, tactics: List[str]) -> str:
    return json.dumps(
        {
            "Incident": incident,
            "MITRE ATT&CK Tactics": tactics,
        },
        ensure_ascii=False,
        indent=2,
    )


def convert_dataset(source_path: Path) -> Tuple[Dict[str, List[str]], Dict[str, int]]:
    raw = json.loads(source_path.read_text(encoding="utf-8"))
    instructions = raw.get("instructions", [])
    answers = raw.get("answers", [])
    if len(instructions) != len(answers):
        raise ValueError("Source dataset has mismatched instructions and answers lengths.")

    out_instructions: List[str] = []
    out_answers: List[str] = []
    stats = {
        "total": len(instructions),
        "kept": 0,
        "skipped_missing_sections": 0,
        "skipped_invalid_answer_json": 0,
        "incident_yes": 0,
        "incident_no": 0,
        "empty_tactics": 0,
    }

    for instruction, answer in zip(instructions, answers):
        system, logs = extract_system_and_logs(str(instruction))
        if not system or not logs:
            stats["skipped_missing_sections"] += 1
            continue

        answer_json = extract_final_json(str(answer))
        if answer_json is None:
            stats["skipped_invalid_answer_json"] += 1
            continue

        incident = normalize_incident(answer_json.get("Incident", ""))
        tactics = normalize_tactics(answer_json.get("MITRE ATT&CK Tactics", []))

        if incident == "Yes":
            stats["incident_yes"] += 1
        elif incident == "No":
            stats["incident_no"] += 1

        if not tactics:
            stats["empty_tactics"] += 1

        out_instructions.append(build_instruction(system, logs))
        out_answers.append(build_answer(incident, tactics))
        stats["kept"] += 1

    return {"instructions": out_instructions, "answers": out_answers}, stats


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Build an Incident+Tactics SFT dataset from incident_examples.json."
    )
    parser.add_argument(
        "--source",
        required=True,
        help="Path to the source incident_examples.json file.",
    )
    parser.add_argument(
        "--output",
        required=True,
        help="Path to write the converted dataset.",
    )
    args = parser.parse_args()

    source_path = Path(args.source)
    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    converted, stats = convert_dataset(source_path)
    serialized = json.dumps(converted, ensure_ascii=False)

    # Write to a temporary file first so interrupted runs do not leave a truncated JSON file behind.
    with tempfile.NamedTemporaryFile(
        "w",
        encoding="utf-8",
        dir=output_path.parent,
        delete=False,
        suffix=output_path.suffix,
    ) as tmp:
        tmp.write(serialized)
        temp_path = Path(tmp.name)

    # Verify the generated file before replacing the target.
    verified = json.loads(temp_path.read_text(encoding="utf-8"))
    if not isinstance(verified, dict):
        temp_path.unlink(missing_ok=True)
        raise ValueError("Converted dataset must be a JSON object.")
    if "instructions" not in verified or "answers" not in verified:
        temp_path.unlink(missing_ok=True)
        raise ValueError("Converted dataset must contain 'instructions' and 'answers'.")
    if len(verified["instructions"]) != len(verified["answers"]):
        temp_path.unlink(missing_ok=True)
        raise ValueError("Converted dataset has mismatched instructions and answers lengths.")

    temp_path.replace(output_path)

    print(f"Wrote incident+tactics dataset to: {output_path}")
    for key, value in stats.items():
        print(f"{key}: {value}")


if __name__ == "__main__":
    main()
