def read_text_arg(inline_value: str | None, file_value: str | None, default_value: str) -> str:
    """Read an inline/file/default text argument."""
    if file_value:
        return Path(file_value).expanduser().read_text(encoding="utf-8").strip()
    if inline_value:
        return inline_value.strip()
    return default_value


def extract_json_object(text: str) -> dict[str, Any] | None:
    decoder = json.JSONDecoder()
    for match in __import__("re").finditer(r"\{", text):
        try:
            obj, _ = decoder.raw_decode(text[match.start():])
        except json.JSONDecodeError:
            continue
        if isinstance(obj, dict):
            return obj
    return None


def normalize_llm_state(current_state: RecoveryState, predicted: dict[str, Any] | None) -> RecoveryState:
    """Normalize an LLM output and enforce monotonic false-to-true state changes."""
    next_state: RecoveryState = {}
    for field in RECOVERY_STATE_FIELDS:
        current_value = bool(current_state.get(field, False))
        predicted_value = bool(predicted.get(field, current_value)) if predicted else current_value
        next_state[field] = current_value or predicted_value
    return next_state


