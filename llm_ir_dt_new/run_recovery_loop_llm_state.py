class LLMStatePredictor:
    """Checkpoint-backed state predictor using the DT state prompt template."""

    def __init__(
        self,
        *,
        checkpoint_path: str,
        context: dict[str, str],
        base_model: str | None = None,
        device_map: str = "auto",
        torch_dtype: str = "float16",
        max_new_tokens: int = 1200,
        temperature: float = 0.0,
        top_p: float = 0.9,
        print_prompts: bool = False,
        omit_logs_when_printing: bool = False,
        shared_model_loader: Any | None = None,
    ) -> None:
        self.checkpoint_path = checkpoint_path
        self.context = context
        self.base_model = base_model
        self.device_map = device_map
        self.torch_dtype = torch_dtype
        self.max_new_tokens = max_new_tokens
        self.temperature = temperature
        self.top_p = top_p
        self.print_prompts = print_prompts
        self.omit_logs_when_printing = omit_logs_when_printing
        self.shared_model_loader = shared_model_loader
        self._tokenizer = None
        self._model = None

    def predict(self, state: RecoveryState, action: HighLevelAction) -> tuple[RecoveryState, str, dict[str, Any] | None]:
        tokenizer, model = self._load()
        prompt = self._build_prompt(state, action)
        if self.print_prompts:
            print("\n===== LLM STATE PROMPT =====")
            print(self._prompt_for_printing(prompt))
            print("===== END LLM STATE PROMPT =====\n")

        inputs = tokenizer(prompt, return_tensors="pt")
        inputs = {key: value.to(model.device) for key, value in inputs.items()}
        do_sample = self.temperature > 0.0
        with torch.no_grad():
            output_ids = model.generate(
                **inputs,
                max_new_tokens=self.max_new_tokens,
                do_sample=do_sample,
                temperature=self.temperature if do_sample else None,
                top_p=self.top_p if do_sample else None,
                pad_token_id=tokenizer.pad_token_id,
                eos_token_id=tokenizer.eos_token_id,
            )

        decoded = tokenizer.decode(output_ids[0], skip_special_tokens=True)
        if decoded.startswith(prompt):
            decoded = decoded[len(prompt):]
        raw_output = decoded.strip()
        parsed = extract_json_object(raw_output)
        return normalize_llm_state(state, parsed), raw_output, parsed

    def _load(self):
        if self._tokenizer is not None and self._model is not None:
            return self._tokenizer, self._model
        if self.shared_model_loader is not None:
            self._tokenizer, self._model = self.shared_model_loader()
            return self._tokenizer, self._model

        dtype = getattr(torch, self.torch_dtype)
        adapter_config_path = Path(self.checkpoint_path) / "adapter_config.json"
        if adapter_config_path.exists():
            adapter_meta = json.loads(adapter_config_path.read_text(encoding="utf-8"))
            base_model_name = self.base_model or str(adapter_meta["base_model_name_or_path"])
            tokenizer = AutoTokenizer.from_pretrained(base_model_name, use_fast=True)
            base_model = AutoModelForCausalLM.from_pretrained(
                base_model_name,
                device_map=self.device_map,
                torch_dtype=dtype,
            )
            model = PeftModel.from_pretrained(base_model, self.checkpoint_path)
        else:
            tokenizer = AutoTokenizer.from_pretrained(self.checkpoint_path, use_fast=True)
            model = AutoModelForCausalLM.from_pretrained(
                self.checkpoint_path,
                device_map=self.device_map,
                torch_dtype=dtype,
            )

        if tokenizer.pad_token is None:
            tokenizer.pad_token = tokenizer.eos_token
        model.eval()
        self._tokenizer = tokenizer
        self._model = model
        return tokenizer, model

    def _build_prompt(self, state: RecoveryState, action: HighLevelAction) -> str:
        state_text = json.dumps(
            {field: bool(state.get(field, False)) for field in RECOVERY_STATE_FIELDS},
            indent=2,
            ensure_ascii=True,
        )
        return STATE_PROMPT_TEMPLATE.format(
            self.context.get("System", "").strip(),
            self.context.get("Logs", "").strip(),
            self.context.get("Incident", "").strip(),
            state_text,
            action.action.strip(),
        ).rstrip()

    def _prompt_for_printing(self, prompt: str) -> str:
        if not self.omit_logs_when_printing:
            return prompt
        logs_text = self.context.get("Logs", "").strip()
        placeholder = (
            "[LOGS OMITTED FROM PRINTED PROMPT: "
            f"{len(logs_text)} characters, {len(logs_text.splitlines())} lines]"
        )
        return prompt.replace(
            f"### Logs:\n{logs_text}\n\n",
            f"### Logs:\n{placeholder}\n\n",
        )


class LLMStateRecoveryLoopOrchestrator(RecoveryLoopOrchestrator):
    """Recovery loop that uses an LLM state predictor instead of state_verifier."""

    def __init__(self, *, state_predictor: LLMStatePredictor, **kwargs: Any) -> None:
        super().__init__(**kwargs)
        self.state_predictor = state_predictor

    def run(self) -> list[CandidateEvaluation]:
        self.plan_store.write_context(self.context)
        selected: list[CandidateEvaluation] = []
        state = initial_recovery_state()

        for step in range(1, self.config.max_plan_steps + 1):
            if is_terminal_state(state):
                break
            candidates = self.action_provider.candidates(
                state,
                self.history_actions,
                self.config.num_candidates,
            )
            evaluations = []
            for idx, candidate in enumerate(candidates, start=1):
                for rollout_idx in range(1, self.config.num_rollouts + 1):
                    evaluations.append(
                        self.evaluate_candidate(
                            step,
                            idx,
                            state,
                            candidate,
                            rollout_index=rollout_idx,
                            rollout_count=self.config.num_rollouts,
                        )
                    )
            for evaluation in evaluations:
                self.plan_store.save_candidate_evaluation(evaluation)

            best = self._select_best_candidate(evaluations)
            if best is None:
                break
            selected.append(best)

            first_result = best.action_results[0] if best.action_results else None
            if first_result is None or best.first_action_plan is None:
                break
            plan = best.first_action_plan
            self.selected_plans.append(plan)
            self.history_actions.append(best.high_level_action)
            self.plan_store.save_selected_plan(step=step, plan=plan, evaluation=best)
            state = dict(first_result.state_after or best.state_after)

        return selected

    def _select_best_candidate(
        self,
        evaluations: list[CandidateEvaluation],
    ) -> CandidateEvaluation | None:
        grouped: dict[int, list[CandidateEvaluation]] = {}
        for evaluation in evaluations:
            grouped.setdefault(evaluation.candidate_index, []).append(evaluation)

        scored: list[tuple[float, float, float, CandidateEvaluation]] = []
        for samples in grouped.values():
            if len(samples) != self.config.num_rollouts:
                continue
            valid_samples = [sample for sample in samples if sample.valid]
            if not valid_samples:
                continue

            terminal_ratio = sum(
                1 for sample in valid_samples if is_terminal_state(sample.state_after)
            ) / len(samples)
            avg_time = (
                sum(sample.rollout_total_time_seconds for sample in valid_samples)
                / len(valid_samples)
            )
            avg_progress = sum(
                state_progress(sample.state_before, sample.state_after)
                for sample in valid_samples
            ) / len(valid_samples)
            representative = min(
                valid_samples,
                key=lambda item: item.rollout_total_time_seconds,
            )
            scored.append(
                (
                    terminal_ratio,
                    avg_time,
                    avg_progress,
                    replace(representative, rollout_total_time_seconds=avg_time),
                )
            )

        if not scored:
            return None
        return min(scored, key=lambda item: (-item[0], item[1], -item[2]))[3]

    def evaluate_candidate(
        self,
        step: int,
        candidate_index: int,
        state: RecoveryState,
        candidate: HighLevelAction,
        rollout_index: int = 1,
        rollout_count: int = 1,
    ) -> CandidateEvaluation:
        wall_start = time.perf_counter()
        _, baseline_time = self.baseline_manager.restore(self.selected_plans)

        action_results: list[ActionExecutionResult] = []
        rollout_state = dict(state)
        next_action = candidate
        first_action_plan: CommandPlan | None = None
        rollout_time = 0.0
        invalid_reason = ""

        for depth in range(self.config.max_rollout_depth):
            del depth
            before = dict(rollout_state)
            try:
                plan = self._plan_for_action(next_action, rollout_state)
                if first_action_plan is None:
                    first_action_plan = plan
                self.safety.validate_plan(plan)
                exec_result = self.executor.execute_plan(plan, state_before=before)
                after, raw_state_output, parsed_state = self.state_predictor.predict(
                    before,
                    next_action,
                )
            except Exception as exc:
                invalid_reason = f"{type(exc).__name__}: {exc}"
                break

            exec_result = ActionExecutionResult(
                high_level_action=exec_result.high_level_action,
                high_level_action_explanation=exec_result.high_level_action_explanation,
                success=exec_result.success,
                action_execution_time_seconds=exec_result.action_execution_time_seconds,
                action_verification_time_seconds=exec_result.action_verification_time_seconds,
                action_total_time_seconds=exec_result.action_total_time_seconds,
                command_results=exec_result.command_results,
                verification_results=exec_result.verification_results,
                state_before=before,
                state_after=after,
            )
            action_results.append(exec_result)
            rollout_time += exec_result.action_total_time_seconds
            self._save_llm_state_prediction(
                step=step,
                candidate_index=candidate_index,
                rollout_index=rollout_index,
                action=next_action,
                state_before=before,
                raw_output=raw_state_output,
                parsed_state=parsed_state,
                state_after=after,
            )

            if not exec_result.success:
                invalid_reason = "command execution failed"
                break
            if has_regression(before, after):
                invalid_reason = "state regression detected"
                break
            rollout_state = after
            if is_terminal_state(rollout_state):
                break
            rollout_candidates = self.action_provider.candidates(
                rollout_state,
                self.history_actions + [next_action],
                1,
            )
            if not rollout_candidates:
                invalid_reason = "no rollout action generated"
                break
            next_action = rollout_candidates[0]

        progress = state_progress(state, rollout_state)
        success = bool(action_results) and all(result.success for result in action_results)
        valid = success and progress > 0 and not invalid_reason
        if not valid and not invalid_reason:
            invalid_reason = "candidate did not improve LLM-predicted recovery state"

        return CandidateEvaluation(
            step=step,
            candidate_index=candidate_index,
            server=self.config.server,
            server_ip=self.config.server_ip,
            high_level_action=candidate,
            success=success,
            valid=valid,
            state_before=state,
            state_after=rollout_state,
            rollout_total_time_seconds=rollout_time,
            baseline_restore_time_seconds=baseline_time,
            wall_clock_time_seconds=time.perf_counter() - wall_start,
            action_results=tuple(action_results),
            first_action_plan=first_action_plan,
            invalid_reason=invalid_reason,
            rollout_index=rollout_index,
            rollout_count=rollout_count,
        )

    def _save_llm_state_prediction(
        self,
        *,
        step: int,
        candidate_index: int,
        rollout_index: int,
        action: HighLevelAction,
        state_before: RecoveryState,
        raw_output: str,
        parsed_state: dict[str, Any] | None,
        state_after: RecoveryState,
    ) -> None:
        state_dir = self.plan_store.run_dir / "llm_state_predictions"
        state_dir.mkdir(parents=True, exist_ok=True)
        path = state_dir / (
            f"step_{step:03d}_candidate_{candidate_index:03d}_"
            f"rollout_{rollout_index:03d}.json"
        )
        payload = {
            "step": step,
            "candidate_index": candidate_index,
            "rollout_index": rollout_index,
            "action": action.action,
            "action_explanation": action.explanation,
            "state_before": state_before,
            "raw_output": raw_output,
            "parsed_state": parsed_state,
            "state_after": state_after,
        }
        path.write_text(json.dumps(payload, indent=2, ensure_ascii=False), encoding="utf-8")


